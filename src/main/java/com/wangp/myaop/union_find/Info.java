public class Info {
    /**
     * 并查集 （Union Find） 也叫不相交集合 （Disjoint Set）
     * 有两个核心操作
     * Union 为连接
     * Find 为查找
     *
     *
     * 两种实现思路:
     * Quick Find
     * 查找（Find)时间复杂度： O(1)
     * 合并 （Union）时间复杂度:O(n)
     *
     * <b> Quick union </b>
     * 查找（Find)时间复杂度： O(α(n)), α(n)<5
     * 合并 （Union）时间复杂度:O(α(n)), α(n)<5
     */
}
